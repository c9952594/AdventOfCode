type Action =
    | Rock
    | Paper
    | Scissors

type TheirAction = Action
type OurAction = Action

type Turn =
    (TheirAction * OurAction)

type InputLine = string

type OurChar = char

type Result =
    | Lose
    | Draw
    | Win

type Points = int

type ParseTheirAction =
    InputLine -> TheirAction

type ParseOurChar =
    InputLine -> OurChar

type OurActionParser =
    TheirAction -> OurChar -> OurAction

type ParseLine =
    OurActionParser -> InputLine -> Turn

type CalculatePoints =
    Turn -> Points




let parseOurChar : ParseOurChar =
    fun line ->
        line[2]

let parseTheirAction : ParseTheirAction =
    fun line ->
        let theirChar = 
            line[0]

        match theirChar with
        | 'A' -> Rock
        | 'B' -> Paper
        | 'C' -> Scissors
        | invalid -> failwith $"Invalid 'their' char '${invalid}'"

let parseAction : OurActionParser =
    fun _ ourChar ->
        match ourChar with
        | 'X' -> Rock
        | 'Y' -> Paper
        | 'Z' -> Scissors
        | invalid -> failwith $"Invalid 'their' char '${invalid}'"

let parseResult : OurActionParser =
    fun theirAction ourChar ->
        let (|Win|Lose|Draw|) =
            function
            | 'X' -> Lose
            | 'Y' -> Draw
            | 'Z' -> Win
            | invalid -> failwith $"Invalid 'their' char '${invalid}'"

        match (theirAction , ourChar) with
        | _ , Draw -> theirAction
        | Rock , Lose -> Scissors
        | Paper , Lose -> Rock
        | Scissors , Lose -> Paper
        | Rock , Win -> Paper
        | Paper , Win -> Scissors
        | Scissors , Win -> Rock

let parseLine : ParseLine =
    fun parser line ->
        let theirAction = 
            parseTheirAction line
                    
        let ourAction =
            parser 
                theirAction 
                (parseOurChar line)

        (theirAction , ourAction)

    
let calculatePoints : CalculatePoints =
    fun turn ->
        let (|Win|Lose|Draw|) =
            function
            | theirs, ours when theirs = ours ->
                Draw
            | Paper, Scissors
            | Scissors, Rock
            | Rock, Paper -> 
                Win
            | _ ->
                Lose

        let winningPoints = 
            match turn with
            | Win -> 6
            | Draw -> 3
            | Lose -> 0
    
        let shapePoints = 
            let ourAction = snd turn
            match ourAction with
            | Rock -> 1
            | Paper -> 2
            | Scissors -> 3

        winningPoints + shapePoints



[<Test>]
let Day02_Part1 () =
    let lineParser =
        parseLine
            parseAction

    let parsed =
        input
        |> Array.map lineParser

    let scores = 
        parsed
        |> Array.map calculatePoints

    let totalScore = 
        scores
        |> Array.sum
        
    Assert.AreEqual(12156, totalScore)

